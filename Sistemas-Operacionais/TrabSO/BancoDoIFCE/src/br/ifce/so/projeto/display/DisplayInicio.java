    private void jButtonIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniciarActionPerformed
        int num;
        try{
            num = Integer.parseInt(jTextField_Caixas.getText());
            if(num > 0 && num <= 5){
                setNumeroDeCaixas(num);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Digite um numero inteiro entre 1 e 5 ","VALOR INCORRETO", JOptionPane.ERROR_MESSAGE);
                jTextField_Caixas.setText(null);
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Digite um numero inteiro ","VALOR INCORRETO", JOptionPane.ERROR_MESSAGE);
            jTextField_Caixas.setText(null);
        }
    }//GEN-LAST:event_jButtonIniciarActionPerformed

